const path = document.getElementById("path");

function renderStations() {
  path.innerHTML = plants.map((p, i) => {
    const isEven = i % 2 === 1;
    const decoIcons = [
      { tag: "leaf-heart", color: "var(--pink)" },
      { tag: "leaf-long", color: "var(--sage)" },
      { tag: "leaf-fern", color: "var(--leaf)" },
      { tag: "leaf-round", color: "var(--gold)" },
      { tag: "leaf-monstera", color: "var(--moss)" },
      { tag: "leaf-heart", color: "var(--terra)" },
      { tag: "leaf-long", color: "var(--moss-deep)" }
    ];
    const deco = decoIcons[i % decoIcons.length];
    return `
      <article class="station ${isEven ? "station--even" : ""}">
        <div class="station__media">
          <svg class="station__media-deco station__media-deco--tl" style="color: ${deco.color}"><use href="#${deco.tag}" /></svg>
          <svg class="station__media-deco station__media-deco--br" style="color: var(--gold)"><use href="#leaf-round" /></svg>
          <img src="${p.image}" alt="${p.name}" loading="lazy" />
        </div>
        <div class="station__body">
          <p class="station__num">остановка ${p.num}</p>
          <h3 class="station__title">${p.name}</h3>
          <p class="station__latin">${p.latin}</p>
          <p class="station__desc">${p.desc}</p>
          <div class="station__tags">
            ${p.tags.map(t => `<span>${t}</span>`).join("")}
          </div>
          <ul class="station__care">
            ${p.care.map(c => `<li>${c}</li>`).join("")}
          </ul>
          <p class="station__where">
            <strong>где искать:</strong> ${p.where}
          </p>
        </div>
      </article>
    `;
  }).join("");

  // Reveal on scroll
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(e => {
      if (e.isIntersecting) {
        e.target.classList.add("is-visible");
        observer.unobserve(e.target);
      }
    });
  }, { threshold: 0.15, rootMargin: "0px 0px -80px 0px" });

  document.querySelectorAll(".station").forEach(s => observer.observe(s));
}

renderStations();
